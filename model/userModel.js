const mongoose = require('mongoose');
const validator = require('validator');

const bcript = require('bcryptjs');

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: [true, 'Please tell us your name!'],
  },
  email: {
    type: String,
    required: [true, 'Please provide your email'],
    unique: true,
    lowercase: true,
    validate: [validator.isEmail, 'Please provide a valid email'],
  },
  photo: String,
  role: {
    type: String,
    enum: ['user', 'guide', 'lead-guide', 'admin'],
    default: 'user',
  },
  password: {
    type: String,
    required: [true, 'Please provide a password'],
    minlength: 8,
    select: false,
  },
  confirmPassword: {
    type: String,
    required: [true, 'Please confirm your password'],
    validate: {
      // This only works on CREATE and SAVE!!!
      validator: function (el) {
        return el === this.password;
      },
      message: 'Passwords are not the same!',
    },
  },
  passwordChangedAt: Date,
  passwordResetToken: String,
  passwordResetExpires: Date,
  active: {
    type: Boolean,
    default: true,
    select: false,
  },
});

userSchema.pre('save', async function (next) {
  if (!this.isModified('password')) return next();
  // hashing the password
  this.password = await bcript.hash(this.password, 12);
  /// delete password confirmation field
  this.confirmPassword = undefined;
  next();
});

//// implementing compare password function

userSchema.methods.correctPassword = async function (
  candidatePassword,
  userPassword
) {
  return bcript.compare(candidatePassword, userPassword);
};

const User = mongoose.model('User', userSchema);

/// CREATING OUR MODEL OUT OF OUR SCHEMA
module.exports = User;
